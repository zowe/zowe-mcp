/** Mutable container for the plugin session state. */
export interface CliPluginState {
  /** Loaded named profiles per type key (e.g. "connection", "location"). */
  profilesByType: Map<string, CliNamedProfile[]>;
  /**
   * Currently active profile ID per type key.
   * For `required: true` types the active ID is set at startup (auto-select or default)
   * or by calling the set tool.
   */
  activeProfileId: Map<string, string | undefined>;
  /**
   * Virtual field context per type key for `perToolOverride: true` types.
   * Updated by the set tool (e.g. endevorSetLocation): primed from a named profile,
   * then individual field overrides applied on top.  Used as defaults for tool calls.
   */
  virtualContextByType: Map<string, Record<string, string | undefined>>;
  /**
   * Resolves password for user@host.  Only needed when at least one profile type
   * has `required: true` and contains a field with `isUsername: true`.
   */
  passwordResolver?: CliPluginPasswordResolver;
  /**
   * Called whenever activeProfileId or virtualContextByType changes.
   * Set by the server to forward the updated active profiles to the VS Code extension.
   */
  onActiveProfilesChanged?: () => void;
  /**
   * Where this plugin's configuration came from.
   * Used to tailor remediation hints in error messages:
   * - 'vscode' — configuration arrived via the VS Code extension pipe (zoweMCP.cliPluginConfiguration)
   * - 'cli'    — configuration came from a file passed via --cli-plugin-configuration / mcp.json
   */
  configSource?: 'vscode' | 'cli';
  /**
   * When set (VS Code mode only), sends a notification to the VS Code extension so the user
   * sees a native error dialog in addition to the in-chat error message.
   *
   * @param message     - Human-readable description of the problem.
   * @param severity    - 'info' | 'warning' | 'error'
   * @param settingsKey - When provided, the notification shows only an "Open Settings" button
   *                      that opens this specific settings key (no "Generate Mock Data" button).
   */
  sendNotification?: (
    message: string,
    severity: 'info' | 'warning' | 'error',
    settingsKey?: string
  ) => void;
  /**
   * Lazily initialised cache shared across all paginated/windowed tools for
   * this plugin instance.  Created on first use by `registerPluginTool()`.
   */
  _cliResultCache?: import('../../zos/response-cache.js').ResponseCache;
  /**
   * Persists the current {@link profilesByType} / {@link activeProfileId} to
   * `zoweMCP.cliPluginConfiguration` (when the VS Code extension is connected) and/or
   * the file passed with `--cli-plugin-configuration`. Set by the server entry point.
   */
  persistProfiles?: () => Promise<void>;
  /** Value of YAML `plugin:` — used as the key for VS Code `cliPluginConfiguration`. */
  pluginKey?: string;
  /** Absolute path from the CLI `--cli-plugin-configuration` file for this plugin. */
  persistConnectionFilePath?: string;
}
