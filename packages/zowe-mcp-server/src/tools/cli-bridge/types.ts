/** A single extra parameter for a tool. */
export interface PluginParam {
  /** MCP parameter name (camelCase). */
  name: string;
  /**
   * CLI option name without '--' (e.g. env, sn, sys, search, data).
   * When set, the value is passed as `--<cliOption> <value>`.
   */
  cliOption?: string;
  /**
   * When true, this parameter is passed as a positional argument
   * (the first bare word after the sub-command), not as --option value.
   */
  cliPositional?: boolean;
  /**
   * Plain description string (fallback for all variants).
   * May be a `$.dotted.path` JSON reference resolved against the companion
   * `<plugin>-commands.json` file. Optional when `descriptions` is provided.
   */
  description?: string;
  /**
   * Per-variant description strings (same resolution order as tool descriptions:
   * active variant → optimized → cli → description plain).
   */
  descriptions?: DescriptionVariants;
  /** Whether this parameter is required. */
  required?: boolean;
  /** Default value if not provided by the caller. */
  default?: string;
  /**
   * Optional mapping from friendly MCP parameter values to CLI argument values.
   *
   * When present, `buildParamSchema` generates a `z.enum` of the map keys (friendly
   * names) wrapped in a `z.preprocess` normalizer that also accepts the raw CLI codes
   * (case-insensitive).  `buildCliArgs` translates the normalized friendly name back
   * to the CLI value before constructing the `--option value` pair.
   *
   * Accepts both friendly names and raw CLI codes (case-insensitive).
   * Invalid values are rejected by Zod with a message listing all valid friendly names.
   *
   * Example: `valueMap: { source: ES, history: EH, changes: EC }`
   * → `'source'`, `'SOURCE'`, `'ES'`, `'es'` all accepted; `'INVALID'` rejected
   * → CLI always receives `--search-in ES` (normalized from any accepted form)
   */
  valueMap?: Record<string, string>;
}

/** A single MCP tool definition in the plugin YAML. */
export interface PluginToolDef {
  /** MCP tool name (camelCase with plugin prefix, e.g. endevorListElements). */
  toolName: string;
  /**
   * The `zowe` command string to execute, e.g. "endevor list elements".
   * The bridge prepends "zowe" and appends "--rfj" plus connection profile args.
   */
  zoweCommand: string;
  /**
   * Which description variant to use for this tool.
   * Falls back to the plugin-level activeDescription, then env ZOWE_MCP_CLI_DESC_VARIANT,
   * then 'optimized', then 'cli', then first available.
   */
  activeDescription?: string;
  /** Description text variants. */
  descriptions: DescriptionVariants;
  /**
   * Resource effect level for capability-tier gating (required).
   *
   * Accepts a name (`none`, `read`, `update`, `delete`, `execute`)
   * or a number (0–4). MCP hints are derived automatically from this
   * value and the active capability tier.
   */
  resourceEffectLevel?: number | string;
  /** @deprecated Use `resourceEffectLevel` instead. Ignored when `resourceEffectLevel` is set. */
  readOnlyHint?: boolean;
  /** @deprecated Use `resourceEffectLevel` instead. Ignored when `resourceEffectLevel` is set. */
  destructiveHint?: boolean;
  /**
   * When true, automatically inject all `perToolOverride: true` profile fields
   * as optional parameters (with virtual context defaults).
   * When a string array, only the named fields are injected.
   */
  locationParams?: boolean | string[];
  /** Additional tool-specific parameters beyond the location params. */
  params?: PluginParam[];
  /**
   * Key to extract from the parsed --rfj JSON response.
   * Default: "data". Use "stdout" for text output (e.g. print element content).
   * Use "." for the entire response body.
   */
  outputPath?: string;
  /**
   * Pagination / line-windowing configuration for this tool.
   *
   * Shorthand values:
   *   - `'list'`    — inherit plugin-level `pagination.list` defaults
   *   - `'content'` — inherit plugin-level `pagination.content` defaults
   *   - `false`     — explicitly opt out of auto-detection
   *
   * Object form: full or partial `PaginationDef`; missing fields filled from
   * the matching plugin-level defaults (`pagination.list` or `pagination.content`).
   *
   * When omitted, auto-detection fires:
   *   - Tools with `outputPath: stdout` → content (when plugin.pagination.content is defined)
   *   - Tools matching `plugin.pagination.list.applyToPattern` → list
   *   - Tools matching `plugin.pagination.content.applyToPattern` → content
   */
  pagination?: ToolPaginationSpec;
  /**
   * When `false`, CLI invocation failures are returned as ordinary tool execution
   * errors (`isError: true`) rather than as fatal configuration errors (`stop: true`).
   *
   * Use for tools where the LLM may send invalid input that causes an error (e.g.
   * `db2ExecuteSql` where a bad SQL statement should be retryable, not fatal).
   * Defaults to `true` (current behaviour — all CLI errors are treated as
   * configuration errors that stop the LLM).
   */
  fatalOnCliError?: boolean;
}

